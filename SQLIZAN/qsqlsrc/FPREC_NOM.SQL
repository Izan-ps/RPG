SET SCHEMA SQLIZAN;
CREATE OR REPLACE FUNCTION ARTPREC (NOM_ART VARCHAR(50)) RETURNS DECIMAL (10,2)
LANGUAGE SQL
BEGIN
DECLARE PREC DECIMAL (10,2);
SELECT PRECIO INTO PREC FROM ARTICULOS WHERE DESCRIPCION = NOM_ART;
RETURN PREC;
END;

