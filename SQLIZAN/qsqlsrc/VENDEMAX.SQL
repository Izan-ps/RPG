-- Unimos tablas cabecera y lineas para obtener los vendedores y cuanto han vendido, agrupamos por vendedor y filtramos por el total maximo que obtenemos de una subconsulta.

CREATE OR REPLACE FUNCTION VENDEDORMAX ()
RETURNS TABLE
(VENDEDOR BIGINT,
TOTAL_VENDIDO BIGINT)
RETURN
(
SELECT T1.VENDEDOR, SUM(T2.UNIDADES) AS TOTAL 
FROM CABECERA T1 JOIN LINEAS T2 ON T1.ALBARAN = T2.ALBARAN 
GROUP BY T1.VENDEDOR 
HAVING SUM(T2.UNIDADES) =
(SELECT MAX(TOTAL) 
FROM (SELECT SUM(L.UNIDADES) AS TOTAL 
FROM CABECERA C JOIN LINEAS L ON C.ALBARAN = L.ALBARAN 
GROUP BY C.VENDEDOR) AS SUBCONSULTA)
);

