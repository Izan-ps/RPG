SET SCHEMA SQLIZAN;
CREATE OR REPLACE FUNCTION FINDDIRBYCLI (NOM_CLI VARCHAR(50)) RETURNS VARCHAR (50)
LANGUAGE SQL
BEGIN
DECLARE DIR VARCHAR (50) DEFAULT 'NO ENCONTRADO';
SELECT DIRECCION INTO DIR FROM CLIENTES
WHERE NOMBRE = NOM_CLI;
RETURN DIR;
END;

