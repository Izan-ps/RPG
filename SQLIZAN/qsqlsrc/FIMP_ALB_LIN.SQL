SET SCHEMA SQLIZAN;
CREATE OR REPLACE FUNCTION FIMPLIN (ALB BIGINT, LIN BIGINT) RETURNS DECIMAL(10,2) 
LANGUAGE SQL
BEGIN
DECLARE IMP DECIMAL(10,2);
SELECT (PRECIO*UNIDADES) INTO IMP FROM LINEAS WHERE ALBARAN=ALB AND LINEA=LIN;
RETURN IMP;
END;

