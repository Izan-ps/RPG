CREATE OR REPLACE FUNCTION TOTALB (ALB BIGINT, FEC DATE) RETURNS DECIMAL (10,2)
LANGUAGE SQL
BEGIN
DECLARE TOT DECIMAL (10,2);
SELECT SUM((UNIDADES*PRECIO)) INTO TOT
FROM LINEAS
WHERE ALBARAN = ALB AND FECHA = FEC;
RETURN TOT;
END;

