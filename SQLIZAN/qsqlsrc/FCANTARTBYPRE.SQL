CREATE OR REPLACE FUNCTION CANTARTBYPRE (PRECIO_ART DECIMAL (10,2)) RETURNS BIGINT
LANGUAGE SQL
BEGIN
DECLARE CANTIDAD BIGINT;
SELECT COUNT(*) INTO CANTIDAD FROM ARTICULOS WHERE PRECIO = PRECIO_ART;
RETURN CANTIDAD;
END;

