CREATE OR REPLACE FUNCTION SQLIZAN.ALBBYIMPANDDATE (IMP DECIMAL (10,2), FEC DATE)
RETURNS TABLE (
Num_ALBARAN BIGINT
)
LANGUAGE SQL
RETURN
SELECT DISTINCT ALBARAN FROM LINEAS WHERE PRECIO*UNIDADES = IMP AND FECHA = FEC;

